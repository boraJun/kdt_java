package variable;
//4번 : 상수
public class Constant {
	public static void main(String[] args) { //main 메소드 중괄호영역 시작
		// final 키워드를 사용한다
		
		final int MAX_STUDENTS = 25; // 정수형 타입의 값을 저장할 수 있는 MAX_STUDENTS 상수를 선언하고, 25라는 값을 MAX_STUDENTS 상수에 대입한다
		System.out.println(MAX_STUDENTS); // MAX_STUDENTS라는 상수에 저장된 25라는 정수값을 콘솔창에 출력하고 줄바꿈한다.
	}  //main 메소드 중괄호 영역 끝
}
